function soma(valor1, valor2) {
    return valor1 + valor2;
}

function multiplica(multi1, multi2) {
    return multi1 * multi2;
};

function divide (divide1, divide2) {
    return divide1 / divide2;
}

// export default é como o PHP  require, porem voce so exporta uma função em vez do arquivo inteiro
export {soma, multiplica, divide};

